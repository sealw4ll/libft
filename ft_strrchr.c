#include "libft.h"

char	*strrchr(const char *s, int c)
{
	int	i;

	i = ft_strlen(s) - 1;
	while (s[i] && i > 0)
	{
		if (*s == c)
			return ((char *)s);
		--i;
	}
	return (NULL);
}
